        <p>Control networks are targeted by the same modern cyber security threats that typical corporate networks face, but many industrial control systems in operation today were designed during a time when it was sufficient for networks to be physically separated ("air-gapped") from their corresponding corporate networks. Designed under a model of implied trust, it was assumed that the only way a system could be on the control network was because it was explicitly authorized to be there. Therefore, it followed that there was no reason to specifically authorize communications between systems. But amidst the sensationalism the Stuxnet worm generated for its ability to sabotage an air-gapped control network, there was an important lesson: Air gaps as a cyber security technique have run their course and are no longer effective.</p>
